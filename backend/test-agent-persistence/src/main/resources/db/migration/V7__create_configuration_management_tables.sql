-- 应用定义表：由外部系统同步，本期平台只读消费
create table applications (
    id bigint generated by default as identity primary key,
    app_id varchar(128) not null unique,
    app_name varchar(255) not null,
    enabled boolean not null default true,
    created_at timestamp not null,
    updated_at timestamp not null
);

-- 应用成员关系表：删除只标记 deleted_at，不影响用户账号或其他应用关系
create table application_members (
    id bigint generated by default as identity primary key,
    app_id varchar(128) not null,
    user_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    deleted_at timestamp,
    constraint fk_application_members_app foreign key (app_id) references applications(app_id),
    constraint fk_application_members_user foreign key (user_id) references users(user_id),
    constraint uk_application_members_app_user unique (app_id, user_id)
);

-- 代码库配置表：Git URL 创建后不允许编辑，不提供删除能力
create table code_repositories (
    id bigint generated by default as identity primary key,
    repository_id varchar(128) not null unique,
    git_url varchar(1024) not null unique,
    name varchar(255) not null,
    standard boolean not null default false,
    created_at timestamp not null,
    updated_at timestamp not null
);

-- 应用与代码库多对多关联表
create table application_repository_links (
    id bigint generated by default as identity primary key,
    app_id varchar(128) not null,
    repository_id varchar(128) not null,
    created_at timestamp not null,
    constraint fk_application_repository_links_app foreign key (app_id) references applications(app_id),
    constraint fk_application_repository_links_repo foreign key (repository_id) references code_repositories(repository_id),
    constraint uk_application_repository_links unique (app_id, repository_id)
);

-- 应用级工作空间配置表，与运行态 workspaces 表相互独立
create table application_workspaces (
    id bigint generated by default as identity primary key,
    workspace_id varchar(128) not null unique,
    app_id varchar(128) not null,
    repository_id varchar(128) not null,
    branch varchar(255) not null,
    directory_path varchar(1024) not null,
    workspace_name varchar(255) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_application_workspaces_app foreign key (app_id) references applications(app_id),
    constraint fk_application_workspaces_repo foreign key (repository_id) references code_repositories(repository_id),
    constraint uk_application_workspaces_location unique (app_id, repository_id, branch, directory_path)
);

-- 用户个人 SSH key 表：一个用户只能保存一把 key，私钥密文存储
create table user_ssh_keys (
    id bigint generated by default as identity primary key,
    ssh_key_id varchar(128) not null unique,
    user_id varchar(128) not null unique,
    name varchar(255) not null,
    fingerprint varchar(255) not null,
    encrypted_private_key text not null,
    encryption_nonce varchar(255) not null,
    created_at timestamp not null,
    constraint fk_user_ssh_keys_user foreign key (user_id) references users(user_id)
);

create index idx_applications_enabled on applications(enabled, app_name);
create index idx_application_members_app_active on application_members(app_id, deleted_at);
create index idx_application_repository_links_repo on application_repository_links(repository_id);
create index idx_application_workspaces_app on application_workspaces(app_id, updated_at);
