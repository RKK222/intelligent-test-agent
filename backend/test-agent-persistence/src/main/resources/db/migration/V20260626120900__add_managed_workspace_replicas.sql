alter table application_workspace_versions add column target_commit_hash varchar(128);
alter table application_workspace_versions add column target_commit_updated_at timestamp;

comment on column application_workspace_versions.target_commit_hash is '应用版本工作区所有服务器副本应同步到的目标 Git commit hash。';
comment on column application_workspace_versions.target_commit_updated_at is '目标 commit 最近更新时间。';

create table application_workspace_version_replicas (
    id bigint generated by default as identity primary key,
    replica_id varchar(128) not null unique,
    version_id varchar(128) not null,
    linux_server_id varchar(128) not null,
    repo_root_path text not null,
    workspace_root_path text not null,
    runtime_workspace_id varchar(128) not null unique,
    current_commit_hash varchar(128),
    sync_status varchar(32) not null,
    last_error text,
    last_synced_at timestamp,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_app_workspace_version_replicas_version foreign key (version_id) references application_workspace_versions(version_id),
    constraint fk_app_workspace_version_replicas_runtime_workspace foreign key (runtime_workspace_id) references workspaces(workspace_id),
    constraint uk_app_workspace_version_replicas_server unique (version_id, linux_server_id)
);

comment on table application_workspace_version_replicas is '应用版本工作区在各 Linux 服务器上的物理副本记录。';
comment on column application_workspace_version_replicas.replica_id is '服务器副本业务 ID。';
comment on column application_workspace_version_replicas.version_id is '关联的应用版本工作区 ID。';
comment on column application_workspace_version_replicas.linux_server_id is '副本所在 Linux 服务器 ID。';
comment on column application_workspace_version_replicas.repo_root_path is '副本所在服务器上的 Git 仓库根路径。';
comment on column application_workspace_version_replicas.workspace_root_path is '副本所在服务器上的 opencode 工作目录路径。';
comment on column application_workspace_version_replicas.runtime_workspace_id is '副本关联的运行态 Workspace ID。';
comment on column application_workspace_version_replicas.current_commit_hash is '该服务器副本当前 Git HEAD commit hash。';
comment on column application_workspace_version_replicas.sync_status is '副本同步状态：PENDING、SYNCING、READY、FAILED。';
comment on column application_workspace_version_replicas.last_error is '最近一次同步失败的脱敏错误摘要。';
comment on column application_workspace_version_replicas.last_synced_at is '最近一次成功同步到目标 commit 的时间。';
comment on column application_workspace_version_replicas.trace_id is '最近一次更新副本记录的 traceId。';

insert into application_workspace_version_replicas(
    replica_id, version_id, linux_server_id, repo_root_path, workspace_root_path,
    runtime_workspace_id, current_commit_hash, sync_status, last_error, last_synced_at,
    trace_id, created_at, updated_at
)
select
    concat('awr_', substring(v.version_id from 5), '_legacy'),
    v.version_id,
    w.linux_server_id,
    v.repo_root_path,
    v.workspace_root_path,
    v.runtime_workspace_id,
    null,
    'PENDING',
    null,
    null,
    w.trace_id,
    v.created_at,
    v.updated_at
from application_workspace_versions v
join workspaces w on w.workspace_id = v.runtime_workspace_id
where w.linux_server_id is not null;

create index idx_app_workspace_version_replicas_server_status
    on application_workspace_version_replicas(linux_server_id, sync_status, updated_at);
create index idx_app_workspace_version_replicas_version_status
    on application_workspace_version_replicas(version_id, sync_status);
