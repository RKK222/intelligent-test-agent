-- 通用 agent 远端会话绑定表：后续新增 agent 不再扩展 sessions 的专有字段。
create table agent_session_bindings (
    id bigint generated by default as identity primary key,
    session_id varchar(128) not null,
    agent_id varchar(64) not null,
    remote_session_id varchar(128) not null,
    execution_node_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    trace_id varchar(128) not null,
    constraint fk_agent_session_bindings_session foreign key (session_id) references sessions(session_id),
    constraint fk_agent_session_bindings_execution_node foreign key (execution_node_id) references execution_nodes(execution_node_id),
    constraint uk_agent_session_bindings_session_agent unique (session_id, agent_id),
    constraint uk_agent_session_bindings_agent_remote unique (agent_id, remote_session_id)
);

create index idx_agent_session_bindings_execution_node on agent_session_bindings(execution_node_id);

insert into agent_session_bindings(
    session_id, agent_id, remote_session_id, execution_node_id, created_at, updated_at, trace_id
)
select
    session_id,
    'opencode',
    opencode_session_id,
    opencode_execution_node_id,
    created_at,
    updated_at,
    trace_id
from sessions
where opencode_session_id is not null
  and opencode_execution_node_id is not null;
