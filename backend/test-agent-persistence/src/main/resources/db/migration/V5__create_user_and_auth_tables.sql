-- 用户表：存储平台用户信息，统一认证号为主键
create table users (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null unique,
    unified_auth_id varchar(255) not null unique,
    username varchar(128) not null unique,
    password_hash varchar(255) not null,
    organization varchar(255),
    rd_department varchar(255),
    department varchar(255),
    status varchar(32) not null default 'ACTIVE',
    created_at timestamp not null,
    updated_at timestamp not null
);

-- 用户登录日志表：记录用户登录时间和浏览器信息
create table user_login_logs (
    id bigint generated by default as identity primary key,
    log_id varchar(128) not null unique,
    user_id varchar(128) not null,
    login_at timestamp not null,
    ip_address varchar(64),
    user_agent text,
    login_result varchar(32) not null,
    constraint fk_user_login_logs_user foreign key (user_id) references users(user_id)
);

-- 通用字典表：存储系统字典数据
create table dictionaries (
    id bigint generated by default as identity primary key,
    dict_id varchar(128) not null unique,
    dict_name varchar(128) not null,
    dict_key varchar(128) not null,
    dict_value varchar(128) not null,
    dict_label varchar(255) not null,
    sort_order integer not null default 0,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint uk_dictionaries_key_value unique (dict_key, dict_value)
);

-- 用户角色对照关系表：关联用户和角色字典
create table user_roles (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null,
    dict_id varchar(128) not null,
    created_at timestamp not null,
    constraint fk_user_roles_user foreign key (user_id) references users(user_id),
    constraint fk_user_roles_dict foreign key (dict_id) references dictionaries(dict_id),
    constraint uk_user_roles_user_dict unique (user_id, dict_id)
);

-- 索引
create index idx_users_unified_auth_id on users(unified_auth_id);
create index idx_users_username on users(username);
create index idx_users_status on users(status);
create index idx_user_login_logs_user on user_login_logs(user_id, login_at);
create index idx_user_login_logs_login_at on user_login_logs(login_at);
create index idx_dictionaries_key on dictionaries(dict_key);
create index idx_user_roles_user on user_roles(user_id);

