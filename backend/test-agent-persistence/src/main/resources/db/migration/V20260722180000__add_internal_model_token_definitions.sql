create table internal_model_tokens (
    token_id bigint generated by default as identity primary key,
    name varchar(256) not null,
    token_value text not null,
    legacy_key varchar(64),
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint uk_internal_model_tokens_name unique (name),
    constraint uk_internal_model_tokens_legacy_key unique (legacy_key),
    constraint ck_internal_model_tokens_name_non_blank check (length(trim(name)) > 0),
    constraint ck_internal_model_tokens_value_non_blank check (length(trim(token_value)) > 0)
);

comment on table internal_model_tokens is '内部模型供应商可复用 Token 定义';
comment on column internal_model_tokens.token_id is '数据库自动生成的稳定 Token 定义主键';
comment on column internal_model_tokens.name is '超级管理员维护的 Token 展示名称';
comment on column internal_model_tokens.token_value is '外部系统提供的 Token 明文，禁止通过 API 或日志返回';
comment on column internal_model_tokens.legacy_key is '旧全局 Token 兼容记录标识，普通 Token 为空';

alter table internal_model_providers
    add column token_id bigint;

alter table internal_model_providers
    add constraint fk_internal_model_providers_token
        foreign key (token_id) references internal_model_tokens(token_id) on delete restrict;

comment on column internal_model_providers.token_id is '供应商选用的可复用 Token 定义主键';

insert into internal_model_tokens (
    name, token_value, legacy_key, created_at, updated_at
)
select
    '默认 Token',
    settings.enterprise_openai_auth_token,
    'default',
    settings.created_at,
    settings.updated_at
from internal_model_proxy_settings settings
where settings.setting_id = 'default'
  and settings.enterprise_openai_auth_token is not null
  and length(trim(settings.enterprise_openai_auth_token)) > 0;

update internal_model_providers
set token_id = (
    select token_id
    from internal_model_tokens
    where legacy_key = 'default'
)
where token_id is null
  and exists (
      select 1
      from internal_model_tokens
      where legacy_key = 'default'
  );
