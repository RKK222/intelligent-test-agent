-- 工作空间创建进度表：同步创建流程写入当前步骤，前端用 operation_id 轮询
create table workspace_create_operations (
    id bigint generated by default as identity primary key,
    operation_id varchar(128) not null unique,
    app_id varchar(128) not null,
    requested_by_user_id varchar(128) not null,
    status varchar(32) not null,
    current_step varchar(64) not null,
    error_code varchar(64),
    error_message varchar(1024),
    workspace_id varchar(128),
    version_id varchar(128),
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_workspace_create_operations_app foreign key (app_id) references applications(app_id),
    constraint fk_workspace_create_operations_user foreign key (requested_by_user_id) references users(user_id)
);

comment on table workspace_create_operations is '设置页创建应用工作空间进度表';
comment on column workspace_create_operations.id is '数据库自增主键';
comment on column workspace_create_operations.operation_id is '前端生成的创建操作ID';
comment on column workspace_create_operations.app_id is '关联应用ID';
comment on column workspace_create_operations.requested_by_user_id is '发起创建的用户ID';
comment on column workspace_create_operations.status is '操作状态：RUNNING/SUCCEEDED/FAILED';
comment on column workspace_create_operations.current_step is '当前步骤';
comment on column workspace_create_operations.error_code is '失败错误码';
comment on column workspace_create_operations.error_message is '安全脱敏后的失败说明';
comment on column workspace_create_operations.workspace_id is '关联应用工作空间模板ID';
comment on column workspace_create_operations.version_id is '关联应用版本工作区ID';
comment on column workspace_create_operations.trace_id is '创建链路traceId';
comment on column workspace_create_operations.created_at is '创建时间';
comment on column workspace_create_operations.updated_at is '更新时间';

create index idx_workspace_create_operations_user_updated on workspace_create_operations(requested_by_user_id, updated_at);

