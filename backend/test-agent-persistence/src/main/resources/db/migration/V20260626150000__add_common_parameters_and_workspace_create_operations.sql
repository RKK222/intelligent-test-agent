-- 通用参数配置表：保存跨模块共享的路径等稳定参数，业务按英文名和平台读取
create table common_parameters (
    id bigint generated by default as identity primary key,
    parameter_id varchar(128) not null unique,
    parameter_english varchar(128) not null,
    parameter_chinese varchar(255) not null,
    parameter_value text not null,
    platform varchar(16) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint ck_common_parameters_platform check (platform in ('windows', 'linux', 'all')),
    constraint uk_common_parameters_name_platform unique (parameter_english, platform)
);

comment on table common_parameters is '通用参数配置表';
comment on column common_parameters.id is '数据库自增主键';
comment on column common_parameters.parameter_id is '参数业务ID';
comment on column common_parameters.parameter_english is '参数英文名';
comment on column common_parameters.parameter_chinese is '参数中文名';
comment on column common_parameters.parameter_value is '参数值';
comment on column common_parameters.platform is '适用平台：windows/linux/all';
comment on column common_parameters.created_at is '创建时间';
comment on column common_parameters.updated_at is '更新时间';

