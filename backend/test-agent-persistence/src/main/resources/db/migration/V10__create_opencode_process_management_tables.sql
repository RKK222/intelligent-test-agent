create table linux_servers (
    id bigint generated by default as identity primary key,
    linux_server_id varchar(64) not null unique,
    name varchar(255) not null,
    status varchar(32) not null,
    capacity_summary_json text not null,
    last_heartbeat_at timestamp not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null
);

create table backend_java_processes (
    id bigint generated by default as identity primary key,
    backend_process_id varchar(128) not null unique,
    linux_server_id varchar(64) not null,
    listen_url varchar(1024) not null,
    status varchar(32) not null,
    started_at timestamp not null,
    last_heartbeat_at timestamp not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_backend_java_processes_linux foreign key (linux_server_id) references linux_servers(linux_server_id)
);

create table opencode_containers (
    id bigint generated by default as identity primary key,
    container_id varchar(128) not null unique,
    linux_server_id varchar(64) not null,
    container_name varchar(255) not null,
    port_start integer not null,
    port_end integer not null,
    max_processes integer not null,
    current_processes integer not null,
    status varchar(32) not null,
    last_heartbeat_at timestamp not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_opencode_containers_linux foreign key (linux_server_id) references linux_servers(linux_server_id),
    constraint chk_opencode_containers_port_range check (port_start >= 1 and port_end <= 65535 and port_start <= port_end),
    constraint chk_opencode_containers_capacity check (
        max_processes >= 1
        and current_processes >= 0
        and current_processes <= max_processes
        and max_processes <= (port_end - port_start + 1)
    )
);

create table opencode_container_managers (
    id bigint generated by default as identity primary key,
    manager_id varchar(128) not null unique,
    container_id varchar(128) not null unique,
    linux_server_id varchar(64) not null,
    protocol_version varchar(64) not null,
    connection_status varchar(32) not null,
    capabilities_json text not null,
    last_heartbeat_at timestamp not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_opencode_container_managers_container foreign key (container_id) references opencode_containers(container_id),
    constraint fk_opencode_container_managers_linux foreign key (linux_server_id) references linux_servers(linux_server_id)
);

create table opencode_manager_backend_connections (
    id bigint generated by default as identity primary key,
    manager_id varchar(128) not null,
    backend_process_id varchar(128) not null,
    status varchar(32) not null,
    connected_at timestamp not null,
    last_heartbeat_at timestamp not null,
    trace_id varchar(128) not null,
    updated_at timestamp not null,
    constraint fk_opencode_manager_connections_manager foreign key (manager_id) references opencode_container_managers(manager_id),
    constraint fk_opencode_manager_connections_backend foreign key (backend_process_id) references backend_java_processes(backend_process_id),
    constraint uk_opencode_manager_backend unique (manager_id, backend_process_id)
);

create table opencode_server_processes (
    id bigint generated by default as identity primary key,
    process_id varchar(128) not null unique,
    user_id varchar(128) not null,
    linux_server_id varchar(64) not null,
    container_id varchar(128) not null,
    port integer not null,
    pid bigint,
    base_url varchar(1024) not null,
    status varchar(32) not null,
    session_path text not null,
    config_path text not null,
    started_at timestamp not null,
    last_health_check_at timestamp not null,
    health_message text,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_opencode_server_processes_user foreign key (user_id) references users(user_id),
    constraint fk_opencode_server_processes_linux foreign key (linux_server_id) references linux_servers(linux_server_id),
    constraint fk_opencode_server_processes_container foreign key (container_id) references opencode_containers(container_id),
    constraint uk_opencode_server_processes_linux_port unique (linux_server_id, port),
    constraint chk_opencode_server_processes_port check (port >= 1 and port <= 65535)
);

create table user_opencode_process_bindings (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null,
    agent_id varchar(64) not null,
    process_id varchar(128) not null unique,
    linux_server_id varchar(64) not null,
    port integer not null,
    status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_user_opencode_bindings_user foreign key (user_id) references users(user_id),
    constraint fk_user_opencode_bindings_process foreign key (process_id) references opencode_server_processes(process_id),
    constraint fk_user_opencode_bindings_linux foreign key (linux_server_id) references linux_servers(linux_server_id),
    constraint uk_user_opencode_bindings_user_agent unique (user_id, agent_id),
    constraint chk_user_opencode_bindings_port check (port >= 1 and port <= 65535)
);

create index idx_linux_servers_status on linux_servers(status, last_heartbeat_at);
create index idx_backend_java_processes_linux on backend_java_processes(linux_server_id, status, last_heartbeat_at);
create index idx_opencode_containers_routable on opencode_containers(status, current_processes, max_processes, updated_at);
create index idx_opencode_managers_connection on opencode_container_managers(connection_status, last_heartbeat_at);
create index idx_opencode_manager_connections_backend on opencode_manager_backend_connections(backend_process_id, status);
create index idx_opencode_server_processes_user on opencode_server_processes(user_id, status, updated_at);
create index idx_opencode_server_processes_container on opencode_server_processes(container_id, status);
create index idx_user_opencode_bindings_user on user_opencode_process_bindings(user_id, status);
