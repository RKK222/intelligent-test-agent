-- 通用参数修改日志表：记录每次参数值修改的审计信息
create table common_parameter_change_logs (
    id bigint generated by default as identity primary key,
    log_id varchar(128) not null unique,
    parameter_id varchar(128) not null,
    old_value text,
    new_value text not null,
    changed_by_user_id varchar(128),
    changed_by_username varchar(255),
    trace_id varchar(128),
    created_at timestamp not null,
    constraint fk_common_parameter_change_logs_parameter
        foreign key (parameter_id) references common_parameters(parameter_id)
);

comment on table common_parameter_change_logs is '通用参数修改日志表';
comment on column common_parameter_change_logs.id is '数据库自增主键';
comment on column common_parameter_change_logs.log_id is '日志业务ID，使用log_前缀';
comment on column common_parameter_change_logs.parameter_id is '关联参数业务ID';
comment on column common_parameter_change_logs.old_value is '修改前的参数值';
comment on column common_parameter_change_logs.new_value is '修改后的参数值';
comment on column common_parameter_change_logs.changed_by_user_id is '修改用户ID';
comment on column common_parameter_change_logs.changed_by_username is '修改用户名';
comment on column common_parameter_change_logs.trace_id is '链路traceId';
comment on column common_parameter_change_logs.created_at is '修改时间';

create index idx_common_parameter_change_logs_parameter on common_parameter_change_logs(parameter_id, created_at desc);
