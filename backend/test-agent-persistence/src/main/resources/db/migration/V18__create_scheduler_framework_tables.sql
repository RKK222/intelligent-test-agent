-- 调度器框架表（H2 / PostgreSQL 兼容）
create table if not exists scheduled_tasks (
    id bigint generated by default as identity primary key,
    task_key varchar(128) not null unique,
    name varchar(255) not null,
    cron_expression varchar(255) not null,
    enabled boolean not null default true,
    lock_ttl_seconds bigint not null,
    next_fire_at timestamp,
    registration_status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint chk_scheduled_tasks_lock_ttl check (lock_ttl_seconds > 0)
);

create table if not exists scheduled_task_plans (
    id bigint generated by default as identity primary key,
    plan_id varchar(128) not null unique,
    task_key varchar(128) not null,
    owner_user_id varchar(128) not null,
    cron_expression varchar(255) not null,
    payload_json text not null,
    enabled boolean not null default true,
    next_fire_at timestamp,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_scheduled_task_plans_task foreign key (task_key) references scheduled_tasks(task_key),
    constraint fk_scheduled_task_plans_owner foreign key (owner_user_id) references users(user_id)
);

create table if not exists scheduled_task_runs (
    id bigint generated by default as identity primary key,
    task_run_id varchar(128) not null unique,
    task_key varchar(128) not null,
    plan_id varchar(128),
    trigger_type varchar(32) not null,
    status varchar(32) not null,
    requested_by_user_id varchar(128),
    scheduled_fire_at timestamp not null,
    started_at timestamp,
    ended_at timestamp,
    owner_instance_id varchar(255),
    skip_reason varchar(255),
    error_code varchar(128),
    error_message text,
    result_json text not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_scheduled_task_runs_task foreign key (task_key) references scheduled_tasks(task_key),
    constraint fk_scheduled_task_runs_plan foreign key (plan_id) references scheduled_task_plans(plan_id),
    constraint fk_scheduled_task_runs_request_user foreign key (requested_by_user_id) references users(user_id)
);

-- sessions 补充列
alter table sessions add column if not exists source_type varchar(32) not null default 'MANUAL';
alter table sessions add column if not exists source_ref_id varchar(128);
alter table sessions add column if not exists created_by_user_id varchar(128);

-- runs 补充列
alter table runs add column if not exists source_type varchar(32) not null default 'MANUAL';
alter table runs add column if not exists source_ref_id varchar(128);
alter table runs add column if not exists triggered_by_user_id varchar(128);

-- session_messages 补充列
alter table session_messages add column if not exists source_type varchar(32) not null default 'MANUAL';
alter table session_messages add column if not exists source_ref_id varchar(128);
alter table session_messages add column if not exists sender_user_id varchar(128);

-- 索引
create index if not exists idx_scheduled_tasks_due on scheduled_tasks(enabled, registration_status, next_fire_at);
create index if not exists idx_scheduled_task_plans_owner on scheduled_task_plans(owner_user_id, enabled, next_fire_at);
create index if not exists idx_scheduled_task_runs_task_status on scheduled_task_runs(task_key, status, updated_at);
create index if not exists idx_scheduled_task_runs_trigger_status on scheduled_task_runs(trigger_type, status, scheduled_fire_at);
create index if not exists idx_scheduled_task_runs_request_user on scheduled_task_runs(requested_by_user_id, updated_at);
create index if not exists idx_sessions_source on sessions(source_type, source_ref_id);
create index if not exists idx_runs_source on runs(source_type, source_ref_id);
create index if not exists idx_session_messages_source on session_messages(source_type, source_ref_id);

-- 外键约束（先删后建保证幂等）
alter table sessions drop constraint if exists fk_sessions_created_by_user;
alter table sessions add constraint fk_sessions_created_by_user foreign key (created_by_user_id) references users(user_id);

alter table runs drop constraint if exists fk_runs_triggered_by_user;
alter table runs add constraint fk_runs_triggered_by_user foreign key (triggered_by_user_id) references users(user_id);

alter table session_messages drop constraint if exists fk_session_messages_sender_user;
alter table session_messages add constraint fk_session_messages_sender_user foreign key (sender_user_id) references users(user_id);
