-- 用户工作区分支偏好表：记录 (user_id, app_id, workspace_id) 维度下用户最近选择的 VCS 分支，
-- 用于下次进入同一工作区时自动展示并对齐本地磁盘分支。同一 (user, app, workspace) 仅保留最新一条。
create table user_workspace_branch_preferences (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null,
    app_id varchar(128) not null,
    workspace_id varchar(128) not null,
    branch varchar(255) not null,
    updated_at timestamp not null,
    constraint fk_user_workspace_branch_preferences_user foreign key (user_id) references users(user_id),
    constraint fk_user_workspace_branch_preferences_app foreign key (app_id) references applications(app_id),
    constraint fk_user_workspace_branch_preferences_workspace foreign key (workspace_id) references workspaces(workspace_id),
    constraint uk_user_workspace_branch_preferences_scope unique (user_id, app_id, workspace_id)
);

create index idx_user_workspace_branch_preferences_user on user_workspace_branch_preferences(user_id, updated_at);
create index idx_user_workspace_branch_preferences_workspace on user_workspace_branch_preferences(workspace_id, updated_at);
