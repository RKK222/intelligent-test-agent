create table opencode_process_start_operations (
    id bigint generated by default as identity primary key,
    operation_id varchar(128) not null unique,
    requested_by_user_id varchar(128) not null,
    agent_id varchar(64) not null,
    status varchar(32) not null,
    current_step varchar(64) not null,
    error_code varchar(64),
    error_message varchar(1024),
    process_id varchar(128),
    service_address varchar(255),
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_opencode_process_start_operations_user
        foreign key (requested_by_user_id) references users(user_id),
    constraint chk_opencode_process_start_operations_status
        check (status in ('RUNNING', 'SUCCEEDED', 'FAILED'))
);

comment on table opencode_process_start_operations is '用户opencode进程初始化进度表';
comment on column opencode_process_start_operations.operation_id is '前端生成的初始化操作ID';
comment on column opencode_process_start_operations.requested_by_user_id is '发起初始化的用户ID';
comment on column opencode_process_start_operations.agent_id is 'Agent ID，目前仅opencode';
comment on column opencode_process_start_operations.status is '操作状态：RUNNING/SUCCEEDED/FAILED';
comment on column opencode_process_start_operations.current_step is '当前启动步骤';
comment on column opencode_process_start_operations.error_code is '失败错误码';
comment on column opencode_process_start_operations.error_message is '安全脱敏后的失败说明';
comment on column opencode_process_start_operations.process_id is '启动成功后的opencode进程ID';
comment on column opencode_process_start_operations.service_address is '启动成功后的服务地址';
comment on column opencode_process_start_operations.trace_id is '链路traceId';

create index idx_opencode_process_start_operations_user_updated
    on opencode_process_start_operations(requested_by_user_id, updated_at);
