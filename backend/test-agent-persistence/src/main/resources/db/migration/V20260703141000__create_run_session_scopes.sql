create table run_session_scopes (
    id bigint generated by default as identity primary key,
    run_id varchar(128) not null unique,
    root_session_id varchar(128) not null,
    scope_version bigint not null,
    trace_id varchar(128) not null,
    metadata_json text not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_run_session_scopes_run foreign key (run_id) references runs(run_id)
);

create table run_session_scope_sessions (
    id bigint generated by default as identity primary key,
    run_id varchar(128) not null,
    session_id varchar(128) not null,
    root_session_id varchar(128) not null,
    parent_session_id varchar(128),
    child_session boolean not null,
    discovery_source varchar(64) not null,
    task_message_id varchar(128),
    task_part_id varchar(128),
    task_call_id varchar(128),
    trace_id varchar(128) not null,
    metadata_json text not null,
    discovered_at timestamp not null,
    updated_at timestamp not null,
    constraint uq_run_session_scope_sessions_run_session unique (run_id, session_id),
    constraint fk_run_session_scope_sessions_scope foreign key (run_id) references run_session_scopes(run_id)
);

create index idx_run_session_scope_sessions_parent on run_session_scope_sessions(run_id, parent_session_id, discovered_at);
create index idx_run_session_scope_sessions_root on run_session_scope_sessions(root_session_id, discovered_at);
create index idx_run_session_scope_sessions_task_part on run_session_scope_sessions(run_id, task_part_id);

