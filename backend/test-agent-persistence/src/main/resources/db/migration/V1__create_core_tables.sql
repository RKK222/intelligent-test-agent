create table workspaces (
    id bigint generated by default as identity primary key,
    workspace_id varchar(128) not null unique,
    name varchar(255) not null,
    root_path text not null,
    status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null
);

create table sessions (
    id bigint generated by default as identity primary key,
    session_id varchar(128) not null unique,
    workspace_id varchar(128) not null,
    title varchar(255) not null,
    status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_sessions_workspace foreign key (workspace_id) references workspaces(workspace_id)
);

create table runs (
    id bigint generated by default as identity primary key,
    run_id varchar(128) not null unique,
    session_id varchar(128) not null,
    workspace_id varchar(128) not null,
    status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_runs_session foreign key (session_id) references sessions(session_id),
    constraint fk_runs_workspace foreign key (workspace_id) references workspaces(workspace_id)
);

create table run_events (
    id bigint generated by default as identity primary key,
    event_id varchar(128) not null unique,
    run_id varchar(128) not null,
    seq bigint not null,
    type varchar(128) not null,
    trace_id varchar(128) not null,
    occurred_at timestamp not null,
    payload_json text not null,
    constraint fk_run_events_run foreign key (run_id) references runs(run_id),
    constraint uq_run_events_run_seq unique (run_id, seq)
);

create table execution_nodes (
    id bigint generated by default as identity primary key,
    execution_node_id varchar(128) not null unique,
    base_url varchar(1024) not null,
    status varchar(32) not null,
    running_runs integer not null,
    max_runs integer not null,
    weight integer not null,
    last_heartbeat_at timestamp not null,
    capabilities_json text not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    updated_at timestamp not null
);

create table routing_decisions (
    id bigint generated by default as identity primary key,
    run_id varchar(128) not null unique,
    execution_node_id varchar(128) not null,
    reason varchar(64) not null,
    decided_at timestamp not null,
    trace_id varchar(128) not null,
    constraint fk_routing_decisions_run foreign key (run_id) references runs(run_id),
    constraint fk_routing_decisions_node foreign key (execution_node_id) references execution_nodes(execution_node_id)
);

create index idx_sessions_workspace on sessions(workspace_id, created_at);
create index idx_runs_session on runs(session_id, created_at);
create index idx_runs_workspace_status on runs(workspace_id, status, created_at);
create index idx_run_events_run_seq on run_events(run_id, seq);
create index idx_execution_nodes_routable on execution_nodes(status, running_runs, max_runs, updated_at);
