create table ai_model_configs (
    id bigint generated by default as identity primary key,
    provider_id varchar(128) not null,
    model_id varchar(255) not null,
    name varchar(255) not null,
    enabled boolean not null default true,
    default_model boolean not null default false,
    input_modalities_json text not null,
    context_limit integer not null,
    output_limit integer not null,
    sort_order integer not null default 100,
    metadata_json text not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint uk_ai_model_configs_provider_model unique (provider_id, model_id)
);

create index idx_ai_model_configs_provider_enabled on ai_model_configs(provider_id, enabled, sort_order);
