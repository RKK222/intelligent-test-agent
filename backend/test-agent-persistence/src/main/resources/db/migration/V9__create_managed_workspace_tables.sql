create table application_workspace_versions (
    id bigint generated by default as identity primary key,
    version_id varchar(128) not null unique,
    application_workspace_id varchar(128) not null,
    app_id varchar(128) not null,
    repository_id varchar(128) not null,
    version varchar(32) not null,
    branch varchar(255) not null,
    repo_root_path text not null,
    workspace_root_path text not null,
    runtime_workspace_id varchar(128) not null unique,
    created_by_user_id varchar(128) not null,
    status varchar(32) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_app_workspace_versions_template foreign key (application_workspace_id) references application_workspaces(workspace_id),
    constraint fk_app_workspace_versions_app foreign key (app_id) references applications(app_id),
    constraint fk_app_workspace_versions_repo foreign key (repository_id) references code_repositories(repository_id),
    constraint fk_app_workspace_versions_runtime_workspace foreign key (runtime_workspace_id) references workspaces(workspace_id),
    constraint fk_app_workspace_versions_created_by foreign key (created_by_user_id) references users(user_id),
    constraint uk_app_workspace_versions_template_version unique (application_workspace_id, version)
);

create table personal_workspaces (
    id bigint generated by default as identity primary key,
    personal_workspace_id varchar(128) not null unique,
    app_workspace_version_id varchar(128) not null,
    app_id varchar(128) not null,
    application_workspace_id varchar(128) not null,
    user_id varchar(128) not null,
    workspace_name varchar(255) not null,
    branch varchar(255) not null,
    repo_root_path text not null,
    workspace_root_path text not null,
    runtime_workspace_id varchar(128) not null unique,
    base_commit varchar(128) not null,
    status varchar(32) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_personal_workspaces_version foreign key (app_workspace_version_id) references application_workspace_versions(version_id),
    constraint fk_personal_workspaces_app foreign key (app_id) references applications(app_id),
    constraint fk_personal_workspaces_template foreign key (application_workspace_id) references application_workspaces(workspace_id),
    constraint fk_personal_workspaces_user foreign key (user_id) references users(user_id),
    constraint fk_personal_workspaces_runtime_workspace foreign key (runtime_workspace_id) references workspaces(workspace_id),
    constraint uk_personal_workspaces_user_name unique (app_workspace_version_id, user_id, workspace_name)
);

create table user_global_workspace_preferences (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null unique,
    workspace_id varchar(128) not null,
    updated_at timestamp not null,
    constraint fk_user_global_workspace_preferences_user foreign key (user_id) references users(user_id),
    constraint fk_user_global_workspace_preferences_workspace foreign key (workspace_id) references workspaces(workspace_id)
);

create table user_application_workspace_preferences (
    id bigint generated by default as identity primary key,
    user_id varchar(128) not null,
    app_id varchar(128) not null,
    workspace_id varchar(128) not null,
    updated_at timestamp not null,
    constraint fk_user_app_workspace_preferences_user foreign key (user_id) references users(user_id),
    constraint fk_user_app_workspace_preferences_app foreign key (app_id) references applications(app_id),
    constraint fk_user_app_workspace_preferences_workspace foreign key (workspace_id) references workspaces(workspace_id),
    constraint uk_user_app_workspace_preferences unique (user_id, app_id)
);

create table workspace_sync_records (
    id bigint generated by default as identity primary key,
    sync_record_id varchar(128) not null unique,
    user_id varchar(128) not null,
    source_workspace_id varchar(128) not null,
    target_workspace_id varchar(128) not null,
    direction varchar(64) not null,
    files_json text not null,
    force boolean not null,
    status varchar(32) not null,
    trace_id varchar(128) not null,
    created_at timestamp not null,
    constraint fk_workspace_sync_records_user foreign key (user_id) references users(user_id),
    constraint fk_workspace_sync_records_source foreign key (source_workspace_id) references workspaces(workspace_id),
    constraint fk_workspace_sync_records_target foreign key (target_workspace_id) references workspaces(workspace_id)
);

create index idx_app_workspace_versions_app on application_workspace_versions(app_id, updated_at);
create index idx_personal_workspaces_user on personal_workspaces(user_id, updated_at);
create index idx_workspace_sync_records_user on workspace_sync_records(user_id, created_at);
