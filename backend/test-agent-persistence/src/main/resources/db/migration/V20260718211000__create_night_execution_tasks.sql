create table night_execution_tasks (
    id bigint generated by default as identity primary key,
    task_id varchar(128) not null unique,
    owner_user_id varchar(128) not null references users(user_id),
    session_id varchar(128) not null references sessions(session_id),
    workspace_id varchar(128) not null references workspaces(workspace_id),
    client_request_id varchar(128) not null,
    session_title varchar(255) not null,
    content_preview varchar(500) not null,
    run_input_json text,
    status varchar(32) not null,
    slot_start timestamp with time zone not null,
    slot_end timestamp with time zone not null,
    window_end timestamp with time zone not null,
    target_linux_server_id varchar(128) not null,
    scheduled_task_run_id varchar(128),
    run_id varchar(128),
    rollover_count integer not null default 0,
    task_created_session boolean not null default false,
    dispatch_started_at timestamp with time zone,
    dismissed_at timestamp with time zone,
    reservation_released_at timestamp with time zone,
    error_code varchar(128),
    error_message varchar(500),
    trace_id varchar(128) not null,
    created_at timestamp with time zone not null,
    updated_at timestamp with time zone not null,
    constraint uk_night_execution_owner_request unique(owner_user_id, client_request_id),
    constraint uk_night_execution_scheduler_run unique(scheduled_task_run_id),
    constraint ck_night_execution_status check(status in ('SCHEDULED','DISPATCHING','DISPATCHED','CANCELLED','FAILED')),
    constraint ck_night_execution_slot check(slot_start < slot_end and slot_end <= window_end)
);

create table night_execution_session_locks (
    session_id varchar(128) primary key references sessions(session_id) on delete cascade,
    task_id varchar(128) not null unique references night_execution_tasks(task_id) on delete cascade,
    owner_user_id varchar(128) not null references users(user_id),
    created_at timestamp with time zone not null
);

create table night_execution_slot_reservations (
    slot_start timestamp with time zone primary key,
    reserved_count integer not null default 0,
    updated_at timestamp with time zone not null,
    constraint ck_night_execution_reserved_count check(reserved_count >= 0)
);

create index idx_night_execution_owner_status_slot on night_execution_tasks(owner_user_id, status, slot_start);
create index idx_night_execution_session_status on night_execution_tasks(session_id, status, updated_at);
create index idx_night_execution_dispatch_started on night_execution_tasks(status, dispatch_started_at);
create index idx_night_execution_scheduled_due on night_execution_tasks(status, slot_start, updated_at);
create index idx_night_execution_status_updated on night_execution_tasks(status, updated_at);

