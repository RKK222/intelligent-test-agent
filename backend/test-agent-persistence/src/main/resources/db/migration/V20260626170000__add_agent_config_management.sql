-- Agent 配置隔离编辑 worktree 记录
create table agent_config_worktrees (
    id bigint generated by default as identity primary key,
    worktree_id varchar(128) not null unique,
    scope varchar(32) not null,
    workspace_id varchar(128),
    worktree_name varchar(128) not null,
    branch varchar(128) not null,
    root_path text not null,
    created_by_user_id varchar(128) not null,
    status varchar(32) not null,
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_agent_config_worktrees_workspace foreign key (workspace_id) references workspaces(workspace_id),
    constraint fk_agent_config_worktrees_user foreign key (created_by_user_id) references users(user_id)
);

comment on table agent_config_worktrees is 'Agent配置Git worktree记录表';
comment on column agent_config_worktrees.worktree_id is 'Agent配置worktree业务ID';
comment on column agent_config_worktrees.scope is '作用域：PUBLIC/WORKSPACE';
comment on column agent_config_worktrees.workspace_id is '工作空间级worktree关联Workspace';
comment on column agent_config_worktrees.worktree_name is '后端拼接日期后的worktree目录名';
comment on column agent_config_worktrees.branch is '本地worktree分支名';
comment on column agent_config_worktrees.root_path is 'worktree根目录';
comment on column agent_config_worktrees.created_by_user_id is '创建人用户ID';
comment on column agent_config_worktrees.status is '状态：ACTIVE/PUBLISHED/REMOVED';

create index idx_agent_config_worktrees_scope_workspace on agent_config_worktrees(scope, workspace_id, status);
create index idx_agent_config_worktrees_user_updated on agent_config_worktrees(created_by_user_id, updated_at);

-- Agent 配置 Git 长操作进度快照，WebSocket 连接先读 snapshot，再接收后续事件
create table agent_config_operations (
    id bigint generated by default as identity primary key,
    operation_id varchar(128) not null unique,
    scope varchar(32) not null,
    workspace_id varchar(128),
    action varchar(64) not null,
    status varchar(32) not null,
    current_step varchar(64) not null,
    error_code varchar(64),
    error_message varchar(1024),
    trace_id varchar(128) not null,
    branch varchar(128),
    commit_hash varchar(128),
    created_at timestamp not null,
    updated_at timestamp not null,
    constraint fk_agent_config_operations_workspace foreign key (workspace_id) references workspaces(workspace_id)
);

comment on table agent_config_operations is 'Agent配置Git长操作进度表';
comment on column agent_config_operations.operation_id is '前端生成或后端补齐的操作ID';
comment on column agent_config_operations.scope is '作用域：PUBLIC/WORKSPACE';
comment on column agent_config_operations.workspace_id is '工作空间级操作关联Workspace';
comment on column agent_config_operations.action is '操作类型：update/create-worktree/commit/publish等';
comment on column agent_config_operations.status is '状态：RUNNING/SUCCEEDED/FAILED';
comment on column agent_config_operations.current_step is '当前步骤';
comment on column agent_config_operations.error_code is '失败错误码';
comment on column agent_config_operations.error_message is '安全脱敏后的失败说明';
comment on column agent_config_operations.trace_id is '链路traceId';
comment on column agent_config_operations.branch is '目标分支';
comment on column agent_config_operations.commit_hash is '完成后的commit hash';

create index idx_agent_config_operations_scope_workspace on agent_config_operations(scope, workspace_id, updated_at);
